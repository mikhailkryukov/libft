#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'a' + 25) || (c >= 'A' && c <= 'A' + 25) ||
		(c >= 'a' && c <= 'a' + 25))
		return (1);
	return (0);
}
